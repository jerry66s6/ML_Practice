import numpy as np

def get_random_subsets(X, y, n_subsets, replacements=True, seed=42):
	# Your code here
    n,m = X.shape[0], X.shape[1]
    np.random.seed(seed)
    out = []
    if n_subsets ==1:
        for i in range(n_subsets):

            index = np.random.choice(n, size = n, replace = replacements)
            Xsub = X[index].tolist()
            ysub = y[index].tolist()
            out.append(Xsub)
            out.append(ysub)
    else:
        for i in range(n_subsets):

            index = np.random.choice(n, size = 2, replace = replacements)
            Xsub = X[index].tolist()
            ysub = y[index].tolist()
            out.append(Xsub)
            out.append(ysub)


    return out