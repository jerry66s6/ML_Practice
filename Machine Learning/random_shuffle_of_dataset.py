import numpy as np

def shuffle_data(X, y, seed=None):
    # Your code here
    if seed is not None:
        np.random.seed(seed)
    index = np.arange(len(y))
    np.random.shuffle(index)
    X = X[index]
    y = y[index]
    return X, y