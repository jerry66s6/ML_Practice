import numpy as np

def batch_iterator(X, y=None, batch_size=64):
    # Your code here
    number = len(X) // batch_size
    out = []

    start = 0
    stop = start + batch_size
    for _ in range(number):
        append_y = []
        if y is not None:
            append_y.append(X[start:stop].tolist())
            append_y.append(y[start:stop].tolist())
            out.append(append_y)
        else:
            out.append(X[start:stop].tolist())
        start = stop
        stop = start + batch_size

    append_y = []
    if y is not None:
        append_y.append(X[start:].tolist())
        append_y.append(y[start:].tolist())
        out.append(append_y)
    else:
        out.append(X[start:].tolist())

    return out