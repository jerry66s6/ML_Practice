import numpy as np

def divide_on_feature(X, feature_i, threshold):
    # Your code here
    column = X[:, feature_i]
    meet = []
    not_meet = []
    for i in range(len(column)):
        if column[i] >= threshold:
            meet.append(X[i, :])
        else:
            not_meet.append(X[i, :])
    return meet, not_meet